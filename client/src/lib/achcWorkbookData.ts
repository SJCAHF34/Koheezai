  export interface WorkbookCheckItem {
    id: string;
    /** Official ACHC standard code from the AHF–ACHC crosswalk (may be empty when the crosswalk row has no assigned standard). */
    standard: string;
    /** Official requirement wording from the crosswalk. */
    requirement: string;
    /** AHF policy reference code(s) (e.g. "I-A-I-4"), comma-separated. */
    ahfPolicy: string;
    /** AHF policy title(s), semicolon-separated. */
    ahfPolicyTitle: string;
    /** Curated AHF action guidance (empty for items without prior curated guidance). */
    ahfAction: string;
    /** Curated AHF evidence guidance (empty for items without prior curated guidance). */
    evidence: string;
    /** Survey evidence-collection methods from the crosswalk (e.g. "P&P · Observation · Interview"). */
    methods: string;
    /** Applicable service lines: SRX, MORX, HME, HIV. */
    services: string[];
    /** Applicable staff roles: PIC, RPh, Tech, PSL. */
    staff: string[];
    /** Source document hyperlink from the AHF–ACHC crosswalk (omitted when the source cell had no link). */
    ahfPolicyUrl?: string;
  }

